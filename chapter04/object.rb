=begin
在 Ruby 中,表现数据的基本单位称为对象(object)
常用的对象的有：

一、数值对象
数字、矩阵、复数、质数、公式

二、字符串对象
字符序列

三、数组对象、散列对象
多个数据的集合

四、正则表达式对象
匹配模式

五、时间对象
日期时间

六、文件对象
对文件进行读写操作

七、符号对象
用于识别方法等名称的标签

八、范围对象(range)

九、异常对象(exception)
